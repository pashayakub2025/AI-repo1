#multiple variables assignment
p,q,r=1,2,3
print(f"value of p is : {p}")
print(f"value of q is : {q}")
print(f"value of r is : {r}")
#swapping variables
m=100
n=200
print(f"Before swapping m is : {m}")
print(f"Before swapping n is : {n}")
m,n=n,m
print(f"After swapping m is : {m}")
print(f"After swapping n is : {n}")
