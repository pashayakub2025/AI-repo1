#assigning variables
x=y=10
print(f"value of x is : {x}")
print(f"value of y is : {y}")
a=5
b=15
print(f"sum of a and b is : {a+b}")
name="Anand"
print(f"Hello, {name}!")
pi=3.14
print(f"value of pi is : {pi}")
is_active=True
