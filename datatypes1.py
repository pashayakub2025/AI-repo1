#String contains a sequence of characters
my_string = "Hello, World!"
print(my_string)

a = "Sam"
word = "Welcome " + a
print(word)

a = 2
b = 3
sum = a + b
print("The sum of a and b is:", sum)
print("The sum of a and b is: " + str(sum))

#Integer contains whole numbers
my_integer = 10
print(my_integer)

#Float contains decimal numbers
my_float = 10.5
print(my_float)
#Boolean contains True or False values
my_boolean = True
print(my_boolean)
my_boolean = False
print(my_boolean)

num1 = 10
num2 = 20
print("The sum of %d and %d is %d" % (num1, num2, num1 + num2))
print("The sum of {} and {} is {}".format(num1, num2, num1 + num2))